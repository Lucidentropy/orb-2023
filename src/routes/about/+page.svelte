    <h2>Origins</h2>

    <p>It was the summer of 2000. Several legendary franchises were in full swing – big names like Counter-Strike, Unreal Tournament, Diablo II, Starcraft, and Quake III. I was trying them all, and one day discovered Starsiege: Tribes, and I was hooked. It had many elements other games didn't have at the time, like outdoor environments, jetpacks, vehicles, dynamic objectives, and custom mods that didn't need to be downloaded first.</p>    
    <p>Before high-speed internet was broadly available, it was important that you found servers with the lowest ping. That is, unless you lived in Hawaii where every server has a high ping. I would hop between servers until I eventually found one with a community I liked playing with: a server called Oklahoma RR Tribes.</p>
    <p>I tried joining other clans at the time but was immediately disappointed at how uncool they were to each other. In the spirit of, "Yeah, well… I’m gonna go build my own clan with blackjack,” I thought it over for a few days, then with a few server regulars in tow, we created Orb. There were 4 founding members: Myself, Lucid (at the time known as Raven); Cells; Doom Train; and Vorax-1.</p>
    <p>Orb has since evolved into a multi-game community with real life meet-ups that have included road trips, weddings, and regular get-togethers with people from numerous states.</p>
    <img src="https://clanorb.s3.us-west-1.amazonaws.com/public/images/calvinhobbesctf.jpg" alt="Calvin and Hobbes stealing the flag."/>
    
    <h2>The Orb philosophy is simple.</h2>
    <p>Orb is a community of people who feel that online gaming is a fun experience to be shared with friends. We're a collection of humans who aren’t perfect, but choose not to accept drama or fighting as the norm. We want others to see us as respectful, funny, mature, and fun to be around. We are people who understand how to have fun and joke without doing it at the expense of others.</p>
    
    <h2>So, what are the rules?</h2>
    <p>Be excellent to each other and don't talk politics.</p>
    
    <h2>What does Orb or <span>{orbTag}</span> mean?</h2>
    <p>In the late '90s, clan tags were filled with 3 letter acronyms, often resulting in some being, for lack of better words, lame things (i.e. x-=SPK=-x for Super Pro Killers). I wanted something that sounded cool, was short, and didn't have a 'l33t' meaning. While trying to buck the trend of silly acronym guild names, I also wanted something simple and symbolic, so I went with {orbTag}. It looks a little cooler in the font used by Tribes. It eventually evolved into people calling us the Tie Fighter clan, due to the tag looking like an ASCII Tie Fighter (from Star Wars.)</p>
    
    <h2>Mental health matters.</h2>
        <p>Difficulties with mental health affect us all. Many gamers find respite from the challenges of mental illness in online gaming and their communities, often even serving as a support group. Orb is a welcoming and accepting community for those who are looking for online friends they can trust to not be an asshat. Whether you're anxious, stressed, depressed, lonely, going through a lot, or just hoping to get a few hours in your chosen game after taking care of the kids, Orb is the group for you.</p>
