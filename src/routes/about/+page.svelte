    <h1>About Orb</h1>
    <p>Orb was founded in the Summer of 2000, based on the simple idea that friends could come together and just
        have fun time playing a game of Tribes. Tired of rude or too serious (sometimes both) clans, me and a few regulars at our favorite server (Oklahoma Tribes RR) agreed that the
        game Tribes is only a game, not a job. Orb was created by just 4 members, Lucid (at the time known as Raven),
        Cells (in rpgs as Ahmetal), Doom Train (also known as Tomb), and Vorax-1.</p>
    <p>The most active contingent of our community has migrated between different games, but generally playing multiple games actively. While our origins are in mod 'Shifter' for Tribes, there are quite a few games
        Orb members play in, in several different genres. While our members span many parts of the country, we have had many meetups and many of us have met in person or attended other's weddings.</p>
    <h2>The Orb philosophy is simple.</h2>
    <p> Orb is about enhancing the game experience shared with friends, friends you can trust to just generally be cool dudes. We're a collection of humans, we can't be perfect, but we work hard to not tolerate any drama or frustration within the community.
        We want others to see us as respectful, funny, mature, and fun to be around. One of the core principles in what it is to be part of the Orb community is
        understanding the balance between maturity and fun. We want Orb members to be proud of the tag, proud of the
        name, and feel like it represents their friends, their humor, and their way of online life regardless if their
        name is at the top or the bottom of the scoreboard.</p>
    <h2>So, what are the rules?</h2>
    <p>Be excellent to each other and don't talk politics.</p>
    <h2>What does Orb or <span>{orbTag}</span> mean?</h2>
    <p>In the late '90s, clan tags were filled with 3 letter acronyms, often meaning some, for lack of better words, lame things (e.x.
        x-=SPK=-x for Super Pro Killers). I wanted something that sounded cool, was short, and didn't have some sort of
        'l33t' meaning. While I was trying to buck the trend of silly acronym guild names, I wanted somethign simple and symbolic, so I went with { orbTag }.
        It looks a little cooler in the font used by Tribes. It eventually evolved into people calling us the Tie Fighter clan.</p>
    <h2>Mental health matters.</h2>
        <p>Difficulties with mental health affect us all, but for many gamers, online gaming and their communities are a respite from the challenges of mental illness. Orb is a welcoming and accepting community for those who are looking for some online friends they can trust to not be an asshat. Wether you're anxious, stressed, going through a lot, or just hoping the few hours you get to play the game after taking care of the kids, is with a group of friends.</p>
