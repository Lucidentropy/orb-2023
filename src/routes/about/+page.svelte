    <h2>Origins</h2>
    <p>It was the summer of 2000, several legendary franchises would be in full swing, big names like Counter-Strike, Unreal Tournament, Diablo II, Starcraft, and Quake III. I was trying them all, and one day discovered Starsiege:Tribes and I was hooked; It had many things other games didn't have at the time: outdoor environments, jetpacks, vehicles, dynamic objectives, and custom mods that didn't need to be downloaded first.</p>
    
    <p>Before cable internet was broadly available, it was important that you found servers with the lowest ping, that is, unless you live in Hawaii where every server has a high ping. I would hop between servers until I found one with a local community I liked playing with, settling on a server called Oklahoma RR Tribes.</p>
    <p>I tried joining other clans at the time, but was immediately dissapointed at how uncool they were to each other. In the spirit of "fine, I'll make my own, with blackjack", I thought it over for a few days, and a few server regulars and I got together and Orb was created, by 4 members, Lucid (at the time known as Raven), Cells, Doom Train, and Vorax-1.</p>
    <p>Orb has since evolved into a multi-game community with real life road trips, weddings, and yearly meetups.</p>
    <img src="https://clanorb.s3.us-west-1.amazonaws.com/public/images/calvinhobbesctf.jpg" alt="Calvin and Hobbes stealing the flag."/>
    <h2>The Orb philosophy is simple.</h2>
    <p> Orb is a community of people who feel that online gaming is a fun experience to be shared with friends. We're a collection of humans, we can't be perfect, but we don't accept drama or fighting as the norm.
        We want others to see us as respectful, funny, mature, and fun to be around, people who understand how to have fun without doing it at the expense of others.</p>
    <h2>So, what are the rules?</h2>
    <p>Be excellent to each other and don't talk politics.</p>
    <h2>What does Orb or <span>{orbTag}</span> mean?</h2>
    <p>In the late '90s, clan tags were filled with 3 letter acronyms, often meaning some, for lack of better words, lame things (e.x.
        x-=SPK=-x for Super Pro Killers). I wanted something that sounded cool, was short, and didn't have a
        'l33t' meaning. While I was trying to buck the trend of silly acronym guild names, I wanted somethign simple and symbolic, so I went with { orbTag }.
        It looks a little cooler in the font used by Tribes. It eventually evolved into people calling us the Tie Fighter clan.</p>
    <h2>Mental health matters.</h2>
        <p>Difficulties with mental health affect us all, but for many gamers, online gaming and their communities are a respite from the challenges of mental illness and for some, even a support group. Orb is a welcoming and accepting community for those who are looking for some online friends they can trust to not be an asshat. Wether you're anxious, stressed, lonely, going through a lot, or just hoping the few hours you get to play the game after taking care of the kids, Orb is a group of friends.</p>
